//生成一些没有必要组件化的简单 html 元素，主要返回其源码字符串，用以加入其他组件中

//链接
function link(url, title) {
    var s = '<a href="' + url+'" >' + title +'</a>';

    return s;
} //

//链接//弹出的
function link_blank(url, title) {
    var s = '<a href="' + url+'" target="_blank">' + title +'</a>';

    return s;
} //



